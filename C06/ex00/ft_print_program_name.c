#include <unistd.h>

int	main(int c, char **arv)
{
	int	i;

	i = 0;
	(void)c;
	while (arv[0][i])
	{
		write(1, &arv[0][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
