#include <unistd.h>

int	main(int c, char **arv)
{
	int	i;
	int	j;

	i = c - 1;
	while (i > 0)
	{
		j = 0;
		while (arv[i][j])
		{
			write(1, &arv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i--;
	}
}
